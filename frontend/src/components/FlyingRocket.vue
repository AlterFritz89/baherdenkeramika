<template>
    <div ref="rocket" class="rocket">üöÄ</div>
  </template>
  
  <script>
  import { gsap } from "gsap";
  
  export default {
    mounted() {
      this.animateRocket();
    },
    methods: {
      animateRocket() {
        const rocket = this.$refs.rocket;
        
        // –°—Ç–∞—Ä—Ç–æ–≤–∞—è –ø–æ–∑–∏—Ü–∏—è (–ª–µ–≤—ã–π –≤–µ—Ä—Ö–Ω–∏–π —É–≥–æ–ª)
        gsap.set(rocket, { x: 0, y: 0 });
  
        const move = () => {
          const x = Math.random() * (window.innerWidth - 50);
          const y = Math.random() * (window.innerHeight - 50);
          const rotation = Math.random() * 360;
          const duration = 5 + Math.random() * 5;
  
          gsap.to(rocket, {
            x,
            y,
            rotation,
            duration,
            ease: "power1.inOut",
            onComplete: move,  // –ó–∞—Ü–∏–∫–ª–∏–≤–∞–µ–º –∞–Ω–∏–º–∞—Ü–∏—é
          });
        };
  
        move();  // –ó–∞–ø—É—Å–∫–∞–µ–º –∞–Ω–∏–º–∞—Ü–∏—é
      },
    },
  };
  </script>
  
